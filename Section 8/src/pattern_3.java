// Nested for loops practice - pattern 3.
public class pattern_3 {
    public static void main(String[] args) {
        for (int i = 1; i<=5; i++) {
            for (int j = 1; j<=5; j++) {
                System.out.print((i+j) + " ");
            }
            System.out.println("");
        }
    }
}



